package org.more.appengine;
/**
 * 标记该类为一个插件。
 * @version : 2013-1-5
 * @author 赵永春 (zyc@byshell.org)
 */
public @interface Plugin {
    /**插件注册的名称。*/
    public String name();
    /**插件启动顺序系统保留值0~1000。启动顺序是按照从小到达的顺序启动。启动顺序相同的程序视类加载先后顺序为准。*/
    public int index() default 0;
}